/**
 * Find help content for a given route path.
 * Supports exact match and prefix match (e.g. /project/xxx matches /project).
 */
export function getHelpForRoute(path) {
  // Exact match first
  if (helpContent[path]) return helpContent[path]

  // Prefix match (e.g. /project/abc -> /project)
  const prefixes = Object.keys(helpContent).sort((a, b) => b.length - a.length)
  for (const prefix of prefixes) {
    if (path.startsWith(prefix)) return helpContent[prefix]
  }

  return null
}
