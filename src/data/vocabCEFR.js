/**
 * Shuffle an array (Fisher-Yates)
 */
function shuffleArray(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

/**
 * Get `count` random words from a specific CEFR level.
 * Returns a shuffled subset without duplicates.
 */
export function getRandomWords(level, count) {
  const pool = vocabByLevel[level]
  if (!pool) return []
  return shuffleArray(pool).slice(0, Math.min(count, pool.length))
}

/**
 * Generate a single question.
 * Returns: { word, correctAnswer, choices[], direction }
 *   direction: 'en-to-th' or 'th-to-en'
 */
export function generateQuestion(level) {
  const pool = vocabByLevel[level]
  if (!pool || pool.length < 4) return null

  const shuffled = shuffleArray(pool)
  const correct = shuffled[0]
  const distractors = shuffled.slice(1, 4)

  const direction = Math.random() < 0.5 ? 'en-to-th' : 'th-to-en'

  const word = direction === 'en-to-th' ? correct.en : correct.th
  const correctAnswer = direction === 'en-to-th' ? correct.th : correct.en

  const choices = shuffleArray([
    correctAnswer,
    ...distractors.map(d => direction === 'en-to-th' ? d.th : d.en)
  ])

  return {
    word,
    correctAnswer,
    choices,
    direction,
    correctIndex: choices.indexOf(correctAnswer)
  }
}

/**
 * Generate multiple questions for a game session.
 * Avoids repeating the same word within a batch.
 */
export function generateQuestionBatch(level, count = 50) {
  const pool = vocabByLevel[level]
  if (!pool || pool.length < 4) return []

  const shuffled = shuffleArray(pool)
  const questions = []

  for (let i = 0; i < Math.min(count, shuffled.length); i++) {
    const correct = shuffled[i]

    const otherWords = pool.filter(w => w.en !== correct.en)
    const distractorPool = shuffleArray(otherWords).slice(0, 3)

    const direction = Math.random() < 0.5 ? 'en-to-th' : 'th-to-en'
    const word = direction === 'en-to-th' ? correct.en : correct.th
    const correctAnswer = direction === 'en-to-th' ? correct.th : correct.en

    const choices = shuffleArray([
      correctAnswer,
      ...distractorPool.map(d => direction === 'en-to-th' ? d.th : d.en)
    ])

    questions.push({
      word,
      correctAnswer,
      choices,
      direction,
      correctIndex: choices.indexOf(correctAnswer)
    })
  }

  return questions
}
